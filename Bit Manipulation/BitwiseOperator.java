public class BitwiseOperator{
    public static void main(String[] args) {
        //Binary And
        System.out.println(5&4);
        //Binary Or
        System.out.println(5|4);
        //Binary XOR
        System.out.println(5^4);
        //Binary Left Shift
        System.out.println(5<<4);
        //Binary Right Shift
        System.out.println(10>>2);
    }
}